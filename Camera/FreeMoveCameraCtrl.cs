using UnityEngine;
using System.Collections;

///////////////////////////////////////////////////////////////////////////////
// \class 
// 
// \brief 
// 
///////////////////////////////////////////////////////////////////////////////

public class FreeMoveCameraCtrl : CameraCtrl {

    ///////////////////////////////////////////////////////////////////////////////
    // functions
    ///////////////////////////////////////////////////////////////////////////////

    // ------------------------------------------------------------------ 
    // Desc: 
    // ------------------------------------------------------------------ 

    void Update () {
        HandleInput ();
    }

    // ------------------------------------------------------------------ 
    // Desc: 
    // ------------------------------------------------------------------ 

    void HandleInput () {
        if ( acceptInput == false )
            return;

        if ( Input.GetMouseButton(1) ) {
            Vector3 eulerAngles = transform.eulerAngles;
            eulerAngles.x -= Input.GetAxis("Mouse Y")*5;
            eulerAngles.y += Input.GetAxis("Mouse X")*5;
            transform.eulerAngles = eulerAngles;
        }
    }
}

